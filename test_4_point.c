#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

int main()
{
	int a = 10;//向内存申请4个字节存储10
	&a;//"&"取地址操作符，取首地址
	printf("%p\n", &a);//16进制展现，每次运行程序变量都会重新创建，变量的地址也会变
	int* p = &a;
	return 0;
}


